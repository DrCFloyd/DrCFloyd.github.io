<h1>Hawthorne's <em>A Wonderbook for Girls and Boys</em> (????, 1903)</h1>

<h2 style="text-align:left">This week, we have two editions of Nathaniel Hawthorne's <em>A Wonderbook for Girls & Boys </em>(or, <em>A Wonderbook for Boys and Girls</em> depending on the edition).

<!--more-->

<p>The first is an undated edition published by Donahue, Henneberry & Co. of Chicago, which was given to me by some close friends. It's a 6" x 4.5", clothbound volume with a beautiful floral design on the cloth and silver, floral stamping  along the inner edge and spine. It looks a lot like a gift book, but is rather plain on the interior. The binding and illustration style make me suspect that it's pre-1900.</p>

<p>There is no title (or text of any kind) on the front cover, only on the spine. The spine also includes Hawthorne's last name and an abbreviated publisher's name.</p>

<p>The text block is top edge gilt. The endpapers are plain, in the same paper as the rest of the text block.</p>

<p>This edition includes a photoxylograph (photographically developed wood engraving) frontispiece portrait of Hawthorne:</p>

